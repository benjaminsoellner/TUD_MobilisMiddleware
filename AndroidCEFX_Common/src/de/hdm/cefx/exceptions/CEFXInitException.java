package de.hdm.cefx.exceptions;


/**
 * The Exception that is thrown if the CEFX initialisation fails.
 * @author Ansgar Gerlicher
 *
 */
@SuppressWarnings("serial")
public class CEFXInitException extends Exception {

	/**
	 * Class constructor.
	 * @param string
	 * @param e
	 */
	public CEFXInitException(String string, Exception e) {
		super(string,e);

	}

	/**
	 * Class constructor.
	 * @param string
	 */
	public CEFXInitException(String string) {
		super(string);
	}

}
