package de.hdm.cefx.exceptions;


/**
 * The Exception that is thrown if a node could not be found.
 * @author Ansgar Gerlicher
 *
 */
@SuppressWarnings("serial")
public class NodeNotFoundException extends Exception {


    /**
     * Class constructor.
     * @param uid UUID of the node that was not found.
     */
    public NodeNotFoundException(String uid)
    {
        super("Node with UID: "+uid+" could not be found in Document");


    }

}
